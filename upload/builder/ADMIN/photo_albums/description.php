		<b>Photo Albums Brief Overview</b>
		<br><br>
		
		The Photo Albums module can be used to show images on the website
		grouped in albums. For every album the administrator can manage
		various settings from the List page such as the home page and the 
		internal pages format, the number of images shown and many others.
		
		
		<br><br>
		In order to use it you need to define a new custom tag and set 
		<font face=Courier>albums.php</font> to be executed for it and then insert this
		tag in your template at the desired position. This will display the list
		with the available albums and when a visitor clicks on the album title,
		he will be able to browse through the images in it.
