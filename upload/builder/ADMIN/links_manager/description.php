		<b>Links Manager Brief Overview</b>
		<br><br>
		
		The Links Manager module provides functionality to display links
		(with their titles and descriptions) on a selected page of the website.
		The links can be grouped in categories. Please not that if no categories
		are defined all the links will show up as a list on the selected page.
		
		<br><br>
		In order to use it you need to set <font face=Courier>links.php</font>
		(LINKS) as extension of a selected page.
