<h4>Anti Money Laundering Laws means </h4>
<p>It is set of procedures, laws or regulations designed to stop the practice of generating income through illegal actions. In most cases money launderers hide their actions through a series of steps that make it look like money coming from illegal or unethical sources was earned legitimately. </p>

<p>AML incorporates a complex of measures aimed at prevention of use of the financial system of the country or any specific financial institution for money laundering or terrorist financing. Such measures and instruments are worked out and implemented by international and national institutions, banking and business community.</p>
<p>Company will always adhere to strict AML policy to prevent money being misused or money  laundering, terrorist financing or other financial crime. Company is fully aware of possible harmful effects and danger to global society, which comes together with the possibility for wrongdoers and criminals to disguise criminal origin of property, transfer, acquire or in any way legalize the ownership of such property.</p>
<p>Company will recognise the importance of complying with all AML laws and combating the financing of terrorism. In dealing with the Company, the User/Distributor acknowledges and agrees that the Company is obliged to adhere to applicable AML Regulations. The User/Distributor will not attempt to initiate any Transactions that may contravene any AML Regulations and will provide such information as is necessary or desirable for the Company to comply with its AML obligations.</p>
<p>Few of the measures which the company will comply with </p>
<ul>
<li>Company will collect identification data of every distributor, as well as IP addresses, online activity, communications and in general all transactions  and pay attention to distributor/user , especially its background, relevant activities and the verification of identity of every distributor for business and all information provided by such distributor</li>
<li>Company will continuously monitoring the activity of distributor and their transactions with the company and shall keep safely all the records on accounts, transactions communications with distributors and company.  </li>
<li>Company will evaluate the possible risk of money laundering while dealing with distributors. </li>
<li>Company will co-operate with responsible AML officers appointed by the Company and approved by AML authorities. </li>
<li>Company will report any suspicious activity to the competent AML authority;</li>
<li>Company will monitor changes to company AML policy in accordance with relevant changes and adopting new measures, if necessary.</li>
<li>Enhanced Due Diligence is exercised on high turn over distributor and /or potential suspicious distributor / transaction.</li>
  </ul>
<p>Information Collection Company reserves the right to collect such information as is necessary from the User/Distributor to meet its obligations under applicable AML Regulations. The Company may pass on information collected from the User /Distributor and relating to Transactions as required by applicable AML Regulations and is under no obligation to inform the User /Distributor it has done so. The Company may undertake all such AML checks in relation to the User/Distributor, or any Transaction as may be deemed necessary or appropriate by the Company, and reserves the right to take any actions (including cancellation of the Transaction, refusal or blocking or access) with no liability to the User/Distributor or any third party.</p>
<p>Transmission of Information to Regulatory Bodies The Company reserves the right to provide all such information regarding the User/Distributor and any Transactions in relation to its obligations to, or requests (whether legally binding or not) by, any relevant regulatory body.</p>
